package Estudos.Dev_Java.numbers_and_strings;

public class PrimitiveTypesNumbers {
    public static void main(
        String... args
    ) {
        int i = 500;
        float gpa = 3.65f;
        byte mask = 0x7f;

        System.out.format(
            "i = %d \ngpa = %f \nmask = %d%n",
            i, gpa, mask
        );
    }
}
